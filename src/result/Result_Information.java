    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String Clase = String.valueOf(className.getSelectedItem());
        String Shift = String.valueOf(shift.getSelectedItem());
        String exameName = String.valueOf(exam.getSelectedItem());
        String Role = role.getText();
        String StudentName = this.studentName.getText();
        String FatherName = this.fatherName.getText();
        String banglaNumber = bangla.getText();
        String englishNumber = english.getText();
        String mathNumber = math.getText();
        String gknoledgelaNumber = gknowledge.getText();
        String religionNumber = religion.getText();
        String sociologyNumber = sociology.getText();
        String TotalNumber = totalNumber.getText();
        String CGPA = cgpa.getText();

        try {
            if (StudentName.isEmpty() || FatherName.isEmpty() || bangla.getText().isEmpty() || english.getText().isEmpty() || math.getText().isEmpty() || sociology.getText().isEmpty() || religion.getText().isEmpty() || gknowledge.getText().isEmpty() || Clase.contains("  ") || Shift.contains("  ") || exameName.contains("  ")) {
                JOptionPane.showMessageDialog(null, "Fill all required field");

            } else {
                // inser data to the table
                String insert = "INSERT INTO  schooldatabase.resultsheet_" + className.getSelectedItem() + "_" + shift.getSelectedItem() + "_" + exam.getSelectedItem() + " VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";   // insert data to Database
                PreparedStatement add = con.prepareStatement(insert);
                add.setInt(1, Integer.parseInt(Role));
                add.setString(2, Clase);
                add.setString(3, Shift);
                add.setString(4, exameName);
                add.setString(5, StudentName);
                add.setString(6, FatherName);
                add.setInt(7, Integer.parseInt(banglaNumber));
                add.setInt(8, Integer.parseInt(englishNumber));
                add.setInt(9, Integer.parseInt(mathNumber));
                add.setInt(10, Integer.parseInt(religionNumber));
                add.setInt(11, Integer.parseInt(gknoledgelaNumber));
                add.setInt(12, Integer.parseInt(sociologyNumber));
                add.setInt(13, Integer.parseInt(TotalNumber));
                add.setDouble(14, Double.parseDouble(grade.getText()));
                add.setString(15, CGPA);
                if (add.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(rootPane, " Student information add successfully");
                }
            }
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            System.out.println(e);
            String s = e.toString();
            String[] ss = s.split(" ");
            if ("Duplicate".equals(ss[1])) {
                JOptionPane.showMessageDialog(null, " already update data againist this role ");
            }
        }
    }//GEN-LAST:event_saveActionPerformed

