public double gradeResult(int number) {
        int numbers = number;
        Double Grade = 0.0;
        String Gpa = "";

        if (numbers > 100) {
            JOptionPane.showMessageDialog(rootPane, "Invalid number");
        } else if (numbers >= 80) {
            Grade = 5.0;
        } else if (numbers >= 70) {
            Grade = 4.0;
        } else if (numbers >= 60) {
            Grade = 3.5;
        } else if (numbers >= 50) {
            Grade = 3.0;
        } else if (numbers >= 40) {
            Grade = 2.5;
        } else if (numbers >= 30) {
            Grade = 2.0;
        } else if (numbers < 30) {
            Grade = 0.0;
        }

        return Grade;
    }

