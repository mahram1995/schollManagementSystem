    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
       
        String exameName = String.valueOf(exam.getSelectedItem());
        String Roll = role.getText();
        String examName=String.valueOf(exam.getSelectedItem());
        String StudentName = this.studentName.getText();
        String FatherName = this.fatherName.getText();
        String banglaNumber = bangla.getText();
        String englishNumber = english.getText();
        String mathNumber = math.getText();
        String gknoledgelaNumber = gknowledge.getText();
        String religionNumber = religion.getText();
        String sociologyNumber = sociology.getText();
        String TotalNumber = totalNumber.getText();
        String CGPA = cgpa.getText();

        try {
            if (StudentName.isEmpty() || FatherName.isEmpty() || bangla.getText().isEmpty() || english.getText().isEmpty() || math.getText().isEmpty() || sociology.getText().isEmpty() || religion.getText().isEmpty() || gknowledge.getText().isEmpty() || exameName.contains("  ")) {
                JOptionPane.showMessageDialog(null, "Fill all required field");

            } else {
                // inser data to the table
                String insert = "INSERT INTO  schooldatabase.resultsheet VALUES (?,?,?,?,?,?,?,?,?,?,?)";   // insert data to Database
                PreparedStatement add = con.prepareStatement(insert);
                add.setInt(1, Integer.parseInt(Roll));
                add.setString(2, examName);
                add.setInt(3, Integer.parseInt(banglaNumber));
                add.setInt(4, Integer.parseInt(englishNumber));
                add.setInt(5, Integer.parseInt(mathNumber));
                add.setInt(6, Integer.parseInt(religionNumber));
                add.setInt(7, Integer.parseInt(gknoledgelaNumber));
                add.setInt(8, Integer.parseInt(sociologyNumber));
                add.setInt(9, Integer.parseInt(TotalNumber));
                add.setDouble(10, Double.parseDouble(grade.getText()));
                add.setString(11, CGPA);
                if (add.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(rootPane, " Student information add successfully");
                }
                clear();
            }
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            System.out.println(e);
            String s = e.toString();
            String[] ss = s.split(" ");
            if ("Duplicate".equals(ss[1])) {
                JOptionPane.showMessageDialog(null, " already update data againist this role ");
            }
        }
    }//GEN-LAST:event_saveActionPerformed

