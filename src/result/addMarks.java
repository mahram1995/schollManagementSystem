    public Double Grade(Double number, String subjectName) {

        String SubjectName = subjectName;
        if ("Bangla".equals(SubjectName)) {
            totalGrade = totalGrade + number - tempGrade1;
            tempGrade1 = number;
        }
        if ("English".equals(SubjectName)) {
            totalGrade = totalGrade + number - tempGrade2;
            tempGrade2 = number;
        }
        if ("Math".equals(SubjectName)) {
            totalGrade = totalGrade + number - tempGrade3;
            tempGrade3 = number;
        }
        if ("Religion".equals(SubjectName)) {
            totalGrade = totalGrade + number - tempGrade4;
            tempGrade4 = number;
        }
        if ("Gknoledge".equals(SubjectName)) {
            totalGrade = totalGrade + number - tempGrade5;
            tempGrade5 = number;
        }
        if ("Socail".equals(SubjectName)) {
            totalGrade = totalGrade + number - tempGrade6;
            tempGrade6 = number;
        }
        double Grade = totalGrade / 6;
        double roundedGrade = (Math.round(Grade * 100.0) / 100.0);

        String CGPA = CGPA_Calculatin(roundedGrade);
        cgpa.setText(CGPA);

        return roundedGrade;
    }

