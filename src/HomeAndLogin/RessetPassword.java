    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:

        try {
            String user = userName.getText();
            String pass = password.getText();
            String cpass = confirmPassword.getText();
            if (pass.endsWith(cpass)) {
                Pattern passwordValidatation = Pattern.compile("^[0-9]{6,12}$");
                Matcher passwordMatcher = passwordValidatation.matcher(password.getText());

                Pattern mobileValidatation = Pattern.compile("^[0-9]{11,11}$");
                Matcher mobileMatcher = mobileValidatation.matcher(mobile.getText());

                if (!mobileMatcher.find()) {
                    JOptionPane.showMessageDialog(this, "Mobile number must be eleven digit");

                } else if (!passwordMatcher.find()) {
                    JOptionPane.showMessageDialog(this, "passwor must be between six to twoelve digit");

                } else {
                    String insert = "update schooldatabase.user_information set password=? where user_name=\"" + user+"\"";

                    //Getting the connection
                    PreparedStatement add = con.prepareStatement(insert);

                    add.setString(1, pass);

                    if (add.executeUpdate() > 0) {
                        JOptionPane.showMessageDialog(this, "Password resset Successfully");
                    }
                }

            } else {
                JOptionPane.showMessageDialog(this, "Password not Match");
            }
        } catch (HeadlessException | SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_saveActionPerformed

