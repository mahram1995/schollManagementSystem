    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String user = userName.getText();
        String pass = password.getText();
        try {
            statement = (Statement) con.createStatement();
            String query = "SELECT * FROM schooldatabase.user_information";
            result = statement.executeQuery(query);
            ArrayList s = new ArrayList();
            ArrayList p = new ArrayList();
            while (result.next()) {
                s.add(result.getString(1));
                p.add(result.getString(2));
            }
            Boolean userNameConfirm = false;
            Boolean passwordNameConfirm = false;
            if (user.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Username");
            } else if (pass.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Password");
            } else {
                for (int i = 0; i < s.size(); i++) {
                    if (user.equals(s.get(i))) {
                        userNameConfirm = true;
                        break;
                    } else if (i == p.size() - 1) {
                        JOptionPane.showMessageDialog(this, "User name incorrect");
                    }
                }
                // check password
                if (userNameConfirm) {
                    for (int i = 0; i < p.size(); i++) {
                        if (pass.equals(p.get(i))) {
                            passwordNameConfirm = true;
                            break;
                        } else if (i == p.size() - 1) {
                            JOptionPane.showMessageDialog(this, "Password incorrect");
                        }
                    }
                }
            }
            if (userNameConfirm & passwordNameConfirm) {
                new Home().setVisible(true);
                dispose();
            }

        } catch (HeadlessException | SQLException e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

